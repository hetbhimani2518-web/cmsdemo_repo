package com.example.cms;

public class StudentProfileDetailsModel {
    private String name , dob , address , contactno , email , program , year , semester , rollno ;

    public StudentProfileDetailsModel() {
    }

    public StudentProfileDetailsModel(String name, String dob,  String address, String contactno, String email, String program, String year, String semester, String rollno) {
        this.name = name;
        this.dob = dob;
        this.address = address;
        this.contactno = contactno;
        this.email = email;
        this.program = program;
        this.year = year;
        this.semester = semester;
        this.rollno = rollno;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDob() {
        return dob;
    }

    public void setDob(String dob) {
        this.dob = dob;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getContactno() {
        return contactno;
    }

    public void setContactno(String contactno) {
        this.contactno = contactno;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getProgram() {
        return program;
    }

    public void setProgram(String program) {
        this.program = program;
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public String getSemester() {
        return semester;
    }

    public void setSemester(String semester) {
        this.semester = semester;
    }

    public String getRollno() {
        return rollno;
    }

    public void setRollno(String rollno) {
        this.rollno = rollno;
    }
}
